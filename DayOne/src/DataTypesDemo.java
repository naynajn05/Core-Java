//Program to demonstrate Data types


public class DataTypesDemo {

	public static void main(String[] args) {

		
		int no=56;
		int value1 = 9 / 2; //integer division
		float value2 = 9676577876f / 2f;
		double value3 = 9676577876d / 2d;
		float value4 = 5/2.5f; //float division
		System.out.println("value 1=" + value1); 
		System.out.println("value 2=" + value2);
		System.out.println("value 3=" + value3);
		System.out.println("value 4=" + value4);
		
		
		int marker = 512;
		//assigning expression to variable 
		double percentage = marker * 0.46f+56;
		System.out.println("percentage :" + percentage);
		
		
	}
}